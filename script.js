  const questionElement = document.getElementById("question");
  const ansBtn = document.getElementById("answer-buttons");
  const nextBtn = document.getElementById("nxt-btn");

  let currentQuestionIndex = 0;
  let score = 0;

  function startQuiz() {
    currentQuestionIndex = 0;
    score = 0;
    nextBtn.innerHTML = "Next";
    showQuestion();
  }

  function showQuestion() {
    resetState();
    let currentQuestion = Questions[currentQuestionIndex];
    questionElement.innerHTML = currentQuestion.question;

    currentQuestion.answers.forEach((answer) => {
      const button = document.createElement("button");
      button.innerHTML = answer.text;
      button.classList.add("btn");
      ansBtn.appendChild(button);
      if (answer.correct) {
        button.dataset.correct = answer.correct;
      }
      button.addEventListener("click", selectAnswer);
    });
  }

  function resetState() {
    nextBtn.classList.add("hide");
    while (ansBtn.firstChild) {
      ansBtn.removeChild(ansBtn.firstChild);
    }
  }

  function selectAnswer(e) {
    const selectedBtn = e.target;
    const isCorrect = selectedBtn.dataset.correct === "true";

    if (isCorrect) {
      selectedBtn.classList.add("correct");
      score++;
    } else {
      selectedBtn.classList.add("wrong");
    }

    // Disable all answer buttons and highlight the correct answer
    Array.from(ansBtn.children).forEach((button) => {
      button.disabled = true;
      if (button.dataset.correct === "true") {
        button.classList.add("correct");
      }
    });

    // Show the Next button to move to the next question
    nextBtn.classList.remove("hide");
  }

  function showScore() {
    resetState();
    questionElement.innerHTML = `You scored ${score} out of ${Questions.length}!`;
    nextBtn.innerHTML = "Play Again";
    nextBtn.classList.remove("hide");
  }

  function handleNextButton() {
    currentQuestionIndex++;
    if (currentQuestionIndex < Questions.length) {
      showQuestion();
    } else {
      showScore();
    }
  }

  nextBtn.addEventListener("click", () => {
    if (currentQuestionIndex < Questions.length) {
      handleNextButton();
    } else {
      startQuiz();
    }
  });

  startQuiz();
